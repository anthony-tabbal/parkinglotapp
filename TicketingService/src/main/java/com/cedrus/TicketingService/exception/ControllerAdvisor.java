package com.cedrus.TicketingService.exception;

import com.cedrus.TicketingService.response.Response;
import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.MethodArgumentNotValidException;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.context.request.WebRequest;
import org.springframework.web.servlet.mvc.method.annotation.ResponseEntityExceptionHandler;

import java.time.LocalDate;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;

@ControllerAdvice
public class ControllerAdvisor extends ResponseEntityExceptionHandler {

    @ExceptionHandler(NoDataFoundException.class)
    public Response<Object> handleNoDataFoundException(
            NoDataFoundException ex, WebRequest request) {

        Response response = new Response();
        response.setStatus(HttpStatus.BAD_REQUEST.toString());
        response.setMessage("No Data Found");

        return response;
    }

    @ExceptionHandler(TicketAlreadyUsedException.class)
    public Response<Object> handleTicketAlreadyUsedException(
            NoDataFoundException ex, WebRequest request) {

        Response response = new Response();
        response.setStatus(HttpStatus.BAD_REQUEST.toString());
        response.setMessage("Ticket is already used");

        return response;
    }

    @ExceptionHandler(CalculateCostException.class)
    public Response<Object> handleTicketCalculateCostException(
            NoDataFoundException ex, WebRequest request) {

        Response response = new Response();
        response.setStatus(HttpStatus.BAD_REQUEST.toString());
        response.setMessage("Not  to calculate cost");

        return response;
    }

    @Override
    protected ResponseEntity<Object> handleMethodArgumentNotValid(
            MethodArgumentNotValidException ex, HttpHeaders headers,
            HttpStatus status, WebRequest request) {

        Map<String, Object> body = new LinkedHashMap<>();
        body.put("timestamp", LocalDate.now());
        body.put("status", status.value());

        List<String> errors = ex.getBindingResult()
                .getFieldErrors()
                .stream()
                .map(x -> x.getDefaultMessage())
                .collect(Collectors.toList());

        body.put("errors", errors);

        return new ResponseEntity<>(body, HttpStatus.BAD_REQUEST);
    }
}
